#include<math.h>
#include<iostream>


int main() {
    std::cout << sqrt(64) << '\n';

    std::cout << round(64) << '\n';
    std::cout << "rounding: " << round(64.2) << '\n';
    std::cout << "rounding: " << round(64.9) << '\n';
    std::cout << "ceil: " << ceil(64.9) << '\n';
    std::cout << "floor: " << floor(64.9) << '\n';
    std::cout << log(64.2) << '\n';
    std::cout << log(10) << '\n';
    std::cout << log(exp(1)) << '\n';
    std::cout << log(10) << '\n';

    std::cout << fabs(-64.2) << '\n';

}

